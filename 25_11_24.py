class Student:
  def __init__(self,name):
    self.name = name
    self.courses = []

  def enroll_course(self,course):
    self.courses.append(course)
    course.enroll_student(self)
    print(f"{self.name} has been enrolled in {course.name}")

class Instructor:
  def __init__(self,name):
    self.name = name
    self.courses = []

  def assign_course(self,course):
    self.courses.append(course)
    print(f"{self.name} has assigned {course.name} as an instructor")


class Course(Student , Instructor):
  def __init__(self,name,duration):
    self.name = name
    self.duration = duration
    self.students = []

  def enroll_student(self,student):
    self.students.append(student)
    print(f"{student.name} has been enrolled in {self.name}")

  def show_course_details(self):
    print(f"Course Name: {self.name}")
    print(f"Duration: {self.duration}")
    print(f"Number of Students: {len(self.students)}")


course = Course("Python Programming","6_Months")
student = Student("Vaishnavi")
instructor = Instructor("Saurabh Sir")

course.enroll_student(student)
instructor.assign_course(course)
course.show_course_details()

